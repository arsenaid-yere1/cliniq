'use server'

import { revalidatePath } from 'next/cache'
import { createClient } from '@/lib/supabase/server'
import {
  createPatientCaseInputSchema,
  type CreatePatientCaseInput,
  editPatientSchema,
  type EditPatientValues,
  editCaseSchema,
  type EditCaseValues,
} from '@/lib/validations/patient'

export async function checkDuplicatePatient(
  firstName: string,
  lastName: string,
  dob: string
) {
  const supabase = await createClient()

  const { data, error } = await supabase
    .from('patients')
    .select('id, first_name, last_name, date_of_birth')
    .ilike('first_name', firstName)
    .ilike('last_name', lastName)
    .eq('date_of_birth', dob)
    .is('deleted_at', null)

  if (error) {
    return { error: error.message, duplicates: [] }
  }

  return { duplicates: data ?? [] }
}

export async function createPatientCase(data: CreatePatientCaseInput) {
  const parsed = createPatientCaseInputSchema.safeParse(data)
  if (!parsed.success) {
    return { error: parsed.error.flatten().fieldErrors }
  }

  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()

  let patientId: string

  if (parsed.data.mode === 'existing_patient') {
    const { data: existing, error: lookupError } = await supabase
      .from('patients')
      .select('id')
      .eq('id', parsed.data.patient_id)
      .is('deleted_at', null)
      .maybeSingle()

    if (lookupError) return { error: lookupError.message }
    if (!existing) return { error: 'Patient not found' }

    patientId = existing.id
  } else {
    const {
      first_name, last_name, middle_name, date_of_birth, gender,
      phone_primary, email, address_line1, address_line2, city, state, zip_code,
    } = parsed.data

    const { data: patient, error: patientError } = await supabase
      .from('patients')
      .insert({
        first_name,
        last_name,
        middle_name: middle_name || null,
        date_of_birth,
        gender: gender || null,
        phone_primary: phone_primary || null,
        email: email || null,
        address_line1: address_line1 || null,
        address_line2: address_line2 || null,
        city: city || null,
        state: state || null,
        zip_code: zip_code || null,
        created_by_user_id: user?.id,
        updated_by_user_id: user?.id,
      })
      .select()
      .single()

    if (patientError) return { error: patientError.message }
    patientId = patient.id
  }

  const {
    accident_date, accident_type, accident_description,
    attorney_id, assigned_provider_id, lien_on_file,
  } = parsed.data

  // Insert case (case_number auto-generated by trigger)
  const { data: caseRecord, error: caseError } = await supabase
    .from('cases')
    .insert({
      patient_id: patientId,
      attorney_id,
      assigned_provider_id,
      accident_date: accident_date || null,
      accident_type: accident_type || null,
      accident_description: accident_description || null,
      lien_on_file: lien_on_file ?? false,
      case_status: 'intake',
      created_by_user_id: user?.id,
      updated_by_user_id: user?.id,
    })
    .select()
    .single()

  if (caseError) {
    return { error: caseError.message }
  }

  // Insert initial status history
  await supabase.from('case_status_history').insert({
    case_id: caseRecord.id,
    new_status: 'intake',
    changed_by_user_id: user?.id,
  })

  revalidatePath('/patients')
  return { data: caseRecord }
}

export async function getPatientForNewCase(patientId: string) {
  const supabase = await createClient()

  const { data, error } = await supabase
    .from('patients')
    .select('id, first_name, last_name, middle_name, date_of_birth, gender')
    .eq('id', patientId)
    .is('deleted_at', null)
    .maybeSingle()

  if (error) return { error: error.message }
  if (!data) return { error: 'Patient not found' }

  return { data }
}

export async function getPatientCase(caseId: string) {
  const supabase = await createClient()

  const { data, error } = await supabase
    .from('cases')
    .select(`
      *,
      patient:patients(*),
      attorney:attorneys(*)
    `)
    .eq('id', caseId)
    .is('deleted_at', null)
    .single()

  if (error) {
    return { error: error.message }
  }

  return { data }
}

export async function listPatientCases(search?: string) {
  const supabase = await createClient()

  let query = supabase
    .from('cases')
    .select(`
      id,
      case_number,
      case_status,
      accident_date,
      created_at,
      patient:patients(id, first_name, last_name)
    `)
    .is('deleted_at', null)
    .order('created_at', { ascending: false })

  if (search) {
    query = query.or(
      `case_number.ilike.%${search}%,patient.first_name.ilike.%${search}%,patient.last_name.ilike.%${search}%`
    )
  }

  const { data, error } = await query

  if (error) {
    return { error: error.message, data: [] }
  }

  // Normalize patient from array to single object (Supabase returns array for relations)
  const normalized = (data ?? []).map((row) => ({
    ...row,
    patient: Array.isArray(row.patient) ? row.patient[0] ?? null : row.patient,
  }))

  return { data: normalized }
}

export async function updatePatient(patientId: string, data: EditPatientValues) {
  const parsed = editPatientSchema.safeParse(data)
  if (!parsed.success) {
    return { error: parsed.error.flatten().fieldErrors }
  }

  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()
  if (!user) return { error: 'Not authenticated' }

  const { error } = await supabase
    .from('patients')
    .update({
      first_name: parsed.data.first_name,
      last_name: parsed.data.last_name,
      middle_name: parsed.data.middle_name || null,
      date_of_birth: parsed.data.date_of_birth,
      gender: parsed.data.gender || null,
      phone_primary: parsed.data.phone_primary || null,
      email: parsed.data.email || null,
      address_line1: parsed.data.address_line1 || null,
      address_line2: parsed.data.address_line2 || null,
      city: parsed.data.city || null,
      state: parsed.data.state || null,
      zip_code: parsed.data.zip_code || null,
      updated_by_user_id: user.id,
    })
    .eq('id', patientId)
    .is('deleted_at', null)

  if (error) return { error: error.message }

  return { data: { success: true } }
}

